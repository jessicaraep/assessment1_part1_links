def all_odd(list):
    """Return a list of only the odd numbers in the input list.

    For example::

        >>> all_odd([1, 2, 7, -5])
        [1, 7, -5]

        >>> all_odd([8, 10, 6, 5])
        [5]
    """
    
    all_odd = []
    for num in list:
        if num % 2 != 0:
            all_odd.append(num)
    return all_odd


